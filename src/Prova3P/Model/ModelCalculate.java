package Prova3P.Model;

public class ModelCalculate {

    public double numbersSum(double firstNum, double secondNum) {
        return firstNum + secondNum;
    }

    public double numberMult(double firstNum, double secondNum) {
        return firstNum * secondNum;
    }

    public double numberDivi(double firstNum, double secondNum) {
        return firstNum / secondNum;
    }

    public double numberSub(double firstNum, double secondNum) {
        return firstNum - secondNum;
    }

}
